////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                      This Function is used to  Reverse the string
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void String_Reverse(char *str)
{
    char *start = str;
    char *end = str;
    char temp = '\0';

    while(*end!='\0')
    {
        end++;
    }
    end--;

    while(start<end)
    {
        temp = *start;
        *start = *end;
        *end = temp;

        start++;
        end--;
    }

}

