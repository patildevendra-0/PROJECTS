/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                          This Function is used to display ASCII Table
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display_ASCII_Table()
{
    int iCnt = 0;
    printf("___________________________________________________________________\n");
    printf("____________________________ASCII TABLE____________________________\n");
    printf("_____________________________________________________________________");

    for(iCnt = 0;iCnt<=127;iCnt++)
    {
        printf("%c\t  %d\t  %o\t  %x\t",iCnt,iCnt,iCnt,iCnt);
        printf("\n");
    }
    printf("_______________________________________________________________________");
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//                          This Function is used to check letter present in string or not
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Check_Letter(char *str,char ch)
{
    bool bFlag = false;

    while(*str != '\0')
    {
        if(*str == ch)
        {
            bFlag = true;
            break;
        }
        str++;
    }
    return bFlag;
}

